/**
 * Умножает два числа.
 * @param {number} a - Первое число для умножения.
 * @param {number} b - Второе число для умножения.
 * @returns {number} - Результат умножения a и b.
 * @param {string} -
 */
function multiply(a, b) {
  return a * b;
}
const multiplyVal = multiply("a", 5);
console.log(multiplyVal);
